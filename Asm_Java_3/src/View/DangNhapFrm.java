    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        try {
            if (check()) {
                String username = txtusername.getText();
                String password = txtpassword.getText();
                Account acc = accountService.findAccount(username, password);
                if (acc == null) {
                    JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại");
                } else {
                    try {
                        switch (acc.getRole()) {
                            
                            case "THU_KHO":
                                JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                                new XuatNhapFrm().setVisible(true);
                                this.dispose();
                                break;
                            case "KINH_DOANH":
                                JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                                new DieuChinhGiaFrm().setVisible(true);
                                this.dispose();
                                break;
                            case "QUAN_LY":
                                JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
                                new QuanLyOcvitFrm().setVisible(true);
                                this.dispose();
                                break;    

                            default:
                                throw new AssertionError();
                        }
                    } catch (Exception e) {
                    }
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(DangNhapFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

