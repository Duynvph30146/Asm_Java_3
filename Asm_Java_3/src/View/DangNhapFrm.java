    public boolean check() {
        if (accountService.CheckName(txtusername) == true) {
            JOptionPane.showMessageDialog(this,"Tên tài khoản không được để trống!", "ERROR",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (accountService.CheckPass(txtpassword) == true) {
            JOptionPane.showMessageDialog(this,"Mật khẩu tài khoản không được để trống!", "ERROR",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

